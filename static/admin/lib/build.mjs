import { readFile, writeFile, mkdir, rm, cp, stat } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import QRCode from 'qrcode';

import { codesJsonSchema } from './schema.mjs';
import { resolveBaseUrl } from './baseUrl.mjs';
import { showAtRef, shortSha } from './git.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '../..');          // static/
const DIST = resolve(ROOT, 'dist');

const QR_OPTIONS = { errorCorrectionLevel: 'H', margin: 2, scale: 8 };

// Cloudflare Pages' published limit is ~2100; leave headroom.
const MAX_REDIRECT_RULES = 2000;

async function pathExists(p) {
  try { await stat(p); return true; } catch { return false; }
}

async function loadCodesJson(ref) {
  const text = ref
    ? await showAtRef(ref, 'static/codes.json')
    : await readFile(resolve(ROOT, 'codes.json'), 'utf8');
  const raw = JSON.parse(text);
  return codesJsonSchema.parse(raw);
}

function escapeHtml(s) {
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function targetUrl(code) {
  return code.type === 'external' ? code.target : `/info/${code.target}.html`;
}

function summaryFor(code) {
  if (code.type === 'internal') return 'intern info-sida';
  try {
    const u = new URL(code.target);
    return u.hostname.replace(/^www\./, '');
  } catch {
    return code.target.slice(0, 40);
  }
}

function renderCard(code) {
  const altLabel = escapeHtml(code.label);
  return `      <article class="card">
        <img src="/qr/${code.slug}.png" alt="QR-kod som länkar till ${altLabel}">
        <h2>${altLabel}</h2>
        <a href="/q/${code.slug}">/q/${code.slug} → ${escapeHtml(summaryFor(code))}</a>
      </article>`;
}

async function emitRedirects(codes, distDir) {
  const lines = [
    '# Generated by admin/lib/build.mjs — do not edit by hand.',
    '# Source of truth: codes.json',
    '',
  ];
  for (const c of codes.filter(c => c.enabled)) {
    lines.push(`/q/${c.slug}\t${targetUrl(c)}\t302`);
  }
  lines.push('');
  lines.push('# Fallback for unknown codes');
  lines.push('/q/*\t/not-found.html\t302');
  lines.push('');
  await writeFile(resolve(distDir, '_redirects'), lines.join('\n'));
}

async function emitIndex(codes, distDir) {
  const template = await readFile(resolve(ROOT, 'index.html'), 'utf8');
  const enabled = codes.filter(c => c.enabled);
  const cards = enabled.length === 0
    ? '      <p class="empty">Inga koder publicerade ännu.</p>'
    : enabled.map(renderCard).join('\n\n');
  if (!template.includes('<!-- {{cards}} -->')) {
    throw new Error('index.html is missing the <!-- {{cards}} --> placeholder');
  }
  const rendered = template.replace('<!-- {{cards}} -->', cards);
  await writeFile(resolve(distDir, 'index.html'), rendered);
}

async function emitNotFound(distDir) {
  await cp(resolve(ROOT, 'not-found.html'), resolve(distDir, 'not-found.html'));
}

async function emitInfo(distDir) {
  const infoSrc = resolve(ROOT, 'info');
  if (await pathExists(infoSrc)) {
    await cp(infoSrc, resolve(distDir, 'info'), { recursive: true });
  }
}

async function emitQrs(codes, distDir, baseUrl) {
  const qrDir = resolve(distDir, 'qr');
  await mkdir(qrDir, { recursive: true });
  for (const c of codes.filter(c => c.enabled)) {
    const url = `${baseUrl}/q/${c.slug}`;
    await QRCode.toFile(resolve(qrDir, `${c.slug}.png`), url, QR_OPTIONS);
    const svg = await QRCode.toString(url, { ...QR_OPTIONS, type: 'svg' });
    await writeFile(resolve(qrDir, `${c.slug}.svg`), svg);
  }
}

async function emitVersion(distDir, { ref, target }) {
  // Best-effort git info. Builds run on the Pi from a tarball with no .git, so
  // never let a failed git call break the build — fall back to nulls.
  let sha = null;
  try {
    sha = await shortSha(ref ?? 'HEAD');
  } catch { /* no git here — leave null */ }

  let version = null;
  try {
    const pkg = JSON.parse(await readFile(resolve(ROOT, 'package.json'), 'utf8'));
    version = pkg.version ?? null;
  } catch { /* no package.json — leave null */ }

  const info = {
    version,
    ref: sha,
    tag: ref && ref.startsWith('release/') ? ref : null,
    target,
    builtAt: new Date().toISOString(),
  };
  await writeFile(resolve(distDir, 'version.json'), JSON.stringify(info, null, 2) + '\n');
  return info;
}

async function checkInternalTargets(codes, ref) {
  if (ref) return; // ref-based build trusts the tagged tree
  const internal = codes.filter(c => c.enabled && c.type === 'internal');
  for (const c of internal) {
    const p = resolve(ROOT, 'info', `${c.target}.html`);
    if (!(await pathExists(p))) {
      throw new Error(
        `code "${c.slug}" (internal) → info/${c.target}.html does not exist on disk`,
      );
    }
  }
}

function checkRedirectCount(codes) {
  const enabled = codes.filter(c => c.enabled).length;
  if (enabled > MAX_REDIRECT_RULES) {
    throw new Error(
      `too many enabled codes (${enabled}); Cloudflare Pages limit is ~2100, our cap is ${MAX_REDIRECT_RULES}`,
    );
  }
}

export async function build({ ref = null, target = 'local' } = {}) {
  const { codes } = await loadCodesJson(ref);
  checkRedirectCount(codes);
  await checkInternalTargets(codes, ref);
  const baseUrl = resolveBaseUrl(target);

  await rm(DIST, { recursive: true, force: true });
  await mkdir(DIST, { recursive: true });

  await emitRedirects(codes, DIST);
  await emitIndex(codes, DIST);
  await emitNotFound(DIST);
  await emitInfo(DIST);
  await emitQrs(codes, DIST, baseUrl);
  const version = await emitVersion(DIST, { ref, target });

  const enabled = codes.filter(c => c.enabled).length;
  const disabled = codes.length - enabled;
  const summary = {
    ref: ref ?? '(working tree)',
    target,
    baseUrl,
    enabled,
    disabled,
    version,
    dist: DIST,
  };
  console.log(`Built ${enabled} enabled · ${disabled} disabled · target=${target} · base=${baseUrl}`);
  console.log(`  ref:     ${summary.ref}`);
  console.log(`  version: ${version.tag ?? version.ref ?? version.version ?? '(unknown)'}`);
  console.log(`  output:  ${DIST}`);
  return summary;
}

function parseArgs(argv) {
  const args = { ref: null, target: 'local' };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--from') args.ref = argv[++i];
    else if (a.startsWith('--from=')) args.ref = a.slice('--from='.length);
    else if (a === '--target') args.target = argv[++i];
    else if (a.startsWith('--target=')) args.target = a.slice('--target='.length);
    else if (a === '--help' || a === '-h') {
      console.log('usage: build [--from <git-ref>] [--target prod|staging|local]');
      process.exit(0);
    } else {
      console.error(`unknown arg: ${a}`);
      process.exit(2);
    }
  }
  return args;
}

if (import.meta.url === `file://${process.argv[1]}`) {
  try {
    await build(parseArgs(process.argv.slice(2)));
  } catch (err) {
    console.error(err.message || err);
    process.exit(1);
  }
}
